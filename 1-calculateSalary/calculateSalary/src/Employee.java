public class Employee {
    
    //Attributes
    public double salary;
    
    public double wage;
    
    //Getters and Setters
    public double getSalary() {
        return salary;
    }
    
    public void setSalary(double salary) {
        this.salary = salary;
    }

    public double getWage() {
        return wage;
    }
    
    
    //Constructors
    public Employee(double salary) {
        this.salary = salary;
    }

}
